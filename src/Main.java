public class Main {
    public static void main(String[] args) {
        System.out.println("10進数の10は:" + 10 + "です。");
        System.out.println("8進数の10は:" + 010 + "です。");
        System.out.println("16進数の10は:" + 0x10 + "です。");
        System.out.println("16進数のFは:" + 0xF + "です。");
    }
}
